
// public 表示公开的
/*
 * class 表示定义一个类
 * A_P2表示一个类名
 */

public class A_P2 {
    /**
     * 类体
     * public 表示公开的
     * static表示静态的
     * void 表示空
     * main表示方法名main
     * （String[] args） 是一个方法的形式参数列表。
     * 以下的方法是一个程序的主方法，是程序的执行入口
     * 是sun公司规定的固定编写方式
     * @param args
     */
    public static void main(String args[]){  //表示定义一个公开的静态的主方法
        //方法体
        /**
         * java语句：
         * Java语句以";"终止，分号必须是半角分号
         * 以下这样的代码的作用是向控制台输出一段消息
         * 以下的双引号必须是半角的双引号，Java语法的一部分
         *
         */
        System.out.println("Hello world");
        System.out.println("你好，jack");

    }
}
