public class FOperator {


    public static void main(String[] args) {
        /*
        关系运算符：
        >   大于
        >=  大于等于
        <   小于
        <=  小于等于
        ==   等于
        ！=  不等于
        关系运算符的运算结果一定是布尔类型：False True
        比较值
         */
        System.out.println("I will learn java well");
        int a = 10;
        int b = 10;
        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a < b);
        System.out.println(a != b);
        // 关系运算符：046
    }


}
